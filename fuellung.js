'use strict'
let bitmap = [
    "................**********........................",
    "...............*..........*.......................",
    "..........*****............*........*.............",
    ".........*.................*.......*.*....*****...",
    "........*................***......*...*.**.....**.",
    "....****.................*.......*.....*.........*",
    "..**......................*******................*",
    ".*...............................................*",
    ".*...............................................*",
    "*...........****.............................****.",
    "*..........*....*.........................***.....",
    ".*.........*....*.......................**........",
    "..***.......****.......................*..........",
    ".....****......................******..*..........",
    ".........**********************.....****.........."
];

bitmap = bitmap.map(el => el.split(''))
//console.log(bitmap)

 const showSignPosition = (x, y) => {
    return bitmap[y][x] 
 }
//console.log(showSignPosition(1, 10))

const changeSign = (x, y) => {
    bitmap[y].splice(x, 1, '*')
    return bitmap.map(el => el.join('')).join('\n')
}
//console.log(changeSign(0, 0))

const floodFill = (x, y) => {
    if (showSignPosition(x, y) === '*') {
       return 
    } if (x >= bitmap[0].length) {
       return
    } if (y >= bitmap.length) {
        return
    } else {
        changeSign(x, y)
    }

    floodFill(x + 1, y)
    floodFill(x, y + 1)
    floodFill(x - 1, y)
    floodFill(x, y - 1)

    return bitmap.map(el => el.join('')).join('\n')
}
console.log(floodFill(8, 7))

